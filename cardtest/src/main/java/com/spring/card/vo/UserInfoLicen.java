package com.spring.card.vo;


public class UserInfoLicen {
	private Integer licen_idx;
	private Integer user_idx;
	private String licen_name;
	private String licen_skill_level;
	public Integer getLicen_idx() {
		return licen_idx;
	}
	public void setLicen_idx(Integer licen_idx) {
		this.licen_idx = licen_idx;
	}
	public Integer getUser_idx() {
		return user_idx;
	}
	public void setUser_idx(Integer user_idx) {
		this.user_idx = user_idx;
	}
	public String getLicen_name() {
		return licen_name;
	}
	public void setLicen_name(String licen_name) {
		this.licen_name = licen_name;
	}
	public String getLicen_skill_level() {
		return licen_skill_level;
	}
	public void setLicen_skill_level(String licen_skill_level) {
		this.licen_skill_level = licen_skill_level;
	}
	
	
	
	
}
