package com.spring.card.vo;

import java.util.Date;


public class UserInfoQualifi {
	private Integer qualifi_idx;
	private Integer user_idx;
	private String qualifi_name;
	private Date qualifi_getdate;
	public Integer getQualifi_idx() {
		return qualifi_idx;
	}
	public void setQualifi_idx(Integer qualifi_idx) {
		this.qualifi_idx = qualifi_idx;
	}
	public Integer getUser_idx() {
		return user_idx;
	}
	public void setUser_idx(Integer user_idx) {
		this.user_idx = user_idx;
	}
	public String getQualifi_name() {
		return qualifi_name;
	}
	public void setQualifi_name(String qualifi_name) {
		this.qualifi_name = qualifi_name;
	}
	public Date getQualifi_getdate() {
		return qualifi_getdate;
	}
	public void setQualifi_getdate(Date qualifi_getdate) {
		this.qualifi_getdate = qualifi_getdate;
	}

	
	
}